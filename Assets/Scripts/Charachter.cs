using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[RequireComponent(typeof(CharacterController))]
[AddComponentMenu("Player Controller/Isometric Character with Jump")]

public class IsometricCharacter : MonoBehaviour
{
    [Header("Movement Settings")]
    public float walkSpeed = 5.0f;
    public float runSpeed = 10.0f;
    public float gravity = -9.81f;        // ???? ??????????
    public float jumpHeight = 2.0f;       // ?????? ??????
    public float rotationSpeed = 720f;    // ???????? ???????? ?????????

    [Header("Camera Settings")]
    public Transform cameraTransform;      // ?????? ?? ??????

    private CharacterController _charController;
    private Vector3 _velocity;             // ??????? ???????? ?????????
    private bool _isGrounded;              // ????, ????????? ?? ???????? ?? ?????
    private bool _isTired = false;
    private float basicSpeed = 5.0f;
    private Stamina _stamina;

    void Start()
    {
        // ???????? ????????? CharacterController
        _charController = GetComponent<CharacterController>();
        _stamina = GetComponent<Stamina>();

        // ???? ?????? ?? ?????? ?? ??????, ?????????? ???????? ??????
        if (cameraTransform == null)
        {
            cameraTransform = Camera.main.transform;
        }
    }

    void FixedUpdate()
    {
        // ????????, ????????? ?? ???????? ?? ?????
        _isGrounded = _charController.isGrounded;
        if (_isGrounded && _velocity.y < 0)
        {
            _velocity.y = -2f; // ????????? ????????????? ???????? ??? ????????????
        }

        // ????????? ????? ?? ??????
        float inputX = Input.GetAxis("Horizontal");
        float inputZ = Input.GetAxis("Vertical");

        // ??????????? ?????? ???????? ? ?????? ??????????? ??????
        Vector3 forward = cameraTransform.forward;
        Vector3 right = cameraTransform.right;

        // ??????? ??????? ????????? ??????
        forward.y = 0;
        right.y = 0;

        forward.Normalize();
        right.Normalize();

        // ???????????? ???????? ?????? ????????
        Vector3 move = (right * inputX + forward * inputZ).normalized;

        // ???? ???? ????????, ???????????? ????????? ? ??????????? ????????
        if (move != Vector3.zero)
        {
            // ???????????? ??????? ???????, ???????? ??? X ? Z, ???????? ?????? ?????? Y
            Quaternion targetRotation = Quaternion.LookRotation(move, Vector3.up);
            transform.rotation = Quaternion.RotateTowards(transform.rotation, targetRotation, rotationSpeed * Time.deltaTime);
        }

        // ?????????? ???????? ????????????
        Vector3 movement = move * walkSpeed;

        // ?????????? ??????????
        _velocity.y += gravity * Time.deltaTime;

        // ????????? ??????
        if (_isGrounded && Input.GetButton("Jump"))
        {
            _velocity.y = Mathf.Sqrt(jumpHeight * -2f * gravity);
        }

        if (Input.GetKey(KeyCode.LeftShift) && !_isTired)
        {
            walkSpeed = runSpeed;
            _stamina.staminaDrain();

            if (_stamina.currentStamina < 1)
                _isTired = true;
        }
        else
        {
            walkSpeed = basicSpeed;
            _stamina.staminaRecovery();
            if (_stamina.currentStamina > 30)
                _isTired = false;
        }

        // ????????? ???????????? ???????? ? ????????
        movement += _velocity;

        // ??????????? ????????? ?????????? ? ?????????? ? ????????? ???????????
        _charController.Move(movement * Time.deltaTime);
    }
}
